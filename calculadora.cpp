#include "calculadora.hpp"

// Suma dos numeros -----------------
double suma(double x, double y)
{
    return x + y;
}

// Resta dos numeros -----------------
double resta(double x, double y)
{
    return x - y;
}

// Multiplica dos numeros -------------
double multiplica(double x, double y)
{
    return x * y;
}

// Divide dos numeros -----------------
double divide(double x, double y)
{
    return x / y;
}